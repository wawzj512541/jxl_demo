package com.lee.jxl;

public class Student {

    String name;
    String sex;
    String age;
    String id;
    String classNo;
    String math;
    String english;
    String chinese;

    public Student(String name, String sex, String age, String id, String classNo, String math, String english, String chinese) {
        this.name = name;
        this.sex = sex;
        this.age = age;
        this.id = id;
        this.classNo = classNo;
        this.math = math;
        this.english = english;
        this.chinese = chinese;
    }
}